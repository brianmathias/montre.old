
/**
 * Enum for five possible drawknob states:
 * 
 * - Off (0)
 * - On (1)
 * - Remove (2)
 * - Add (3)
 * - OutOfRange (4)
 */
export enum DrawknobState {
    Off, // 0
    On, // 1
    Remove, // 2
    Add, // 3
    OutOfRange // 4
  }

  export enum DrawknobType {
    Stop, // 0
    Aux, // 1
    Exp // 2
  }